// Operações com variáveis tipo number
let x = 2;
x = x + 1;

console.log(x); // 3

let y = 2;
y = y / 8;
console.log(y); // 0.25

let z = 2;
z = z * 7;
console.log(z); // 14


// Operações com variáveis tipo string
let nomeStr = "Pedro";
nomeStr = nomeStr + 8;

console.log(nomeStr); // Pedro8


let nome = "Pedro";
let sobrenome = "Nascimento";

let nomeCompleto = nome + " " + sobrenome;
console.log(nomeCompleto); // Pedro Nascimento